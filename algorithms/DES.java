    private byte[] permuteThroughtList(int[] pcList, byte[] target) {
        byte[] result = new byte[pcList.length];
        for (int i = 0; i < pcList.length; i++) {
            int bitPos = pcList[i] - 1; // adjust to 0-based index
            int byteIndex = bitPos / 8; // byte index
            int bitIndex = bitPos % 8; // bit position within byte
            
            if (bitPos + 1 > target.length * 8) result[i] = (byte) 0;
            else result[i] = (byte) ((target[byteIndex] >> (7 - bitIndex)) & 1);
        }
        return result;
    }
